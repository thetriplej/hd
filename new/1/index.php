$(document).ready(function(){

	owlSingle.on('changed.owl.carousel', function(e) {
		var this_item=$(this).find('.active .product-item');
		if(this_item.attr('id')){
			$('#product_name').text($(this).find('.active .product-item').attr('product_name'));
			$('#product_code').text($(this).find('.active .product-item').attr('product_code'));
			$('#product_color').text($(this).find('.active .product-item').attr('product_color'));
			$('#color_thumb').attr('src', "images/colors/thumb/"+$(this).find('.active .product-item').attr('color_thumb')+".jpg");
			$('#catch_phrase').html($(this).find('.active .product-item').attr('catch_phrase'));
		}
	});

	$('.news-carousel').hover(function () {
		newsCarousel.trigger('stop.owl.autoplay');
	});


	function stopYutube(){
		var v;
		$(".embed-responsive-item").each(function( index ) {
			v= $(this).attr("src");
			$(this).attr("src","");
			$(this).attr("src",v);
		});
	}

	newsCarousel.on('changed.owl.carousel', function(e) {
		stopYutube();
	});

	$('.view-more').on('click', function () {
		stopYutube();
	});


	$(window).scroll( function(){
		$('.slide_left').each( function(i){
			var bottom_of_element = $(this).offset().top + $(this).outerHeight();
			var bottom_of_window = $(window).scrollTop() + $(window).height();
			if( bottom_of_window > bottom_of_element ){
				$(this).animate({'opacity':'1','margin-left':'0px'},1500);
			}
		}); 
		$('.slide_bottom').each( function(i){
			var bottom_of_element = $(this).offset().top + $(this).outerHeight();
			var bottom_of_window = $(window).scrollTop() + $(window).height()+400;
			if( bottom_of_window > bottom_of_element ){
				$(this).animate({'opacity':'1','margin-top':'0px'},500+parseInt($(this).attr('diff')));
			}
		}); 
	});
	if(parseInt($(window).height())>1000){
		$('.slide_left').each( function(i){
			$(this).animate({'opacity':'1','margin-left':'0px'},1000);
		});
	}
});
</script>


